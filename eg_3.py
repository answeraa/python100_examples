# 问题：一个整数，它加上100后是一个完全平方数，再加上168又是一个完全平方数，请问该数是多少？
# 思路：设该整数为x，则由已知条件可知x+100=m^2,x+100+168=n^2
#       n^2-m^2=(n+m)(n-m)=168,令n+m=i,n-m=j,则m=(i-j)/2,n=(i+j)/2,i*j=168
#       由以上条件可知i和j均是>=2的偶数。
#       故当i>=2时，2<=j<=168/2
for j in range(2, 85):
    if 168 % j == 0:
        i = 168/j
        if i > j and (i-j) % 2 == 0 and (i+j) % 2 == 0:
            m = int((i-j)/2)
            n = int((i+j)/2)
